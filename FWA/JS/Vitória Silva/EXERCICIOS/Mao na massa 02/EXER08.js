let num1  = parseInt(prompt("Insira sua nota:"))
switch (num1) {
    case   9 |   10:
        console.log("A");
    break;
    case   7 |   8:
        console.log("B");
    break;
    case   5 |   6:
        console.log("C");
    break;
    case   3 |   4:
        console.log("D");
    break;
    case   0 |   2:
        console.log("F");
    break;
}