/* 2.	Crie um programa que verifica se o número é 
positivo, negativo ou zero. Se for positivo, deve
imprimir "X é positivo", onde X é o número
fornecido. Se for negativo, deve imprimir "X é 
negativo". Se for zero, imprima "X é zero". */
let X = 0
