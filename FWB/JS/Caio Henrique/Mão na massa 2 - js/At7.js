var operador = "/";
var numero1 = 5;
var numero2 = 2;

switch (operador) {
   case "+":
      console.log(numero1 + numero2);
      break;
   case "-":
      console.log(numero1 - numero2);
      break;
   case "*":
      console.log(numero1 * numero2);
      break;
   case "/":
      console.log(numero1 / numero2);
      break;
}
